package br.ufpb.miniteste.aps;

public abstract class Carro {
	
	public String marca;
	public int ano;
	
	
	
	public String getMarca() {
		return marca;
	}
	public void setMarca(String marca) {
		this.marca = marca;
	}
	public int getAno() {
		return ano;
	}
	public void setAno(int ano) {
		this.ano = ano;
	}
	
	
	

}
